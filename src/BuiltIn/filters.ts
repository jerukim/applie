type MinMax = { min: number; max: number }

export type BuiltInJobFilters = {
  earlyApplicant?: boolean
  datePosted?: DatePosted
  jobCategory?: JobCategory
  jobSubCategory?: Array<
    | CyberSecuritySubCategory
    | DataAnalyticsSubCategory
    | DeveloperEngineerSubCategory
    | OperationsSubCategory
    | SalesSubCategory
  >
  skills?: Array<
    | DeveloperEngineerSkills
    | CyberSecuritySkills
    | DataAnalyticsSkills
  >
  experience?: Experience[]
  industry?: Industry[]
  companySize?: MinMax
}

export function companySizeRangeToInputId({ min, max }: MinMax) {
  if (min <= 0) min = 1
  if (max <= 0) max = Infinity

  return [
    { min: 1, max: 10, value: CompanySize['1-10'] },
    { min: 11, max: 50, value: CompanySize['11-50'] },
    { min: 51, max: 200, value: CompanySize['51-200'] },
    { min: 201, max: 500, value: CompanySize['201-500'] },
    { min: 501, max: 1000, value: CompanySize['501-1000'] },
    { min: 1000, max: Infinity, value: CompanySize['1000+'] },
  ]
    .filter(
      (r) =>
        (min >= r.min && max <= r.max) || // min and max falls within range
        (min <= r.min && max >= r.max) || // min and max overlaps range
        (min >= r.min && min <= r.max) || // min falls within range
        (max >= r.min && max <= r.max) // max falls within range
    )
    .map((r) => r.value)
}
